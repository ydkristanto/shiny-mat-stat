# Peladen ----
server <- function(input, output) {
  # Negara berdasarkan benua
  afrika <- pilihan_negara_benua %>%
    filter(benua == "Afrika")
  amerika_selatan <- pilihan_negara_benua %>%
    filter(benua == "Amerika Selatan")
  amerika_utara <- pilihan_negara_benua %>%
    filter(benua == "Amerika Utara")
  asia <- pilihan_negara_benua %>%
    filter(benua == "Asia")
  eropa <- pilihan_negara_benua %>%
    filter(benua == "Eropa")
  oseania <- pilihan_negara_benua %>%
    filter(benua == "Oseania")
  # Daftar pilihan negara
  pilihan_input_negara <- list(
    Afrika = as.list(setNames(afrika$kode, afrika$negara)),
    `Amerika Selatan` = as.list(setNames(
      amerika_selatan$kode,
      amerika_selatan$negara
    )),
    `Amerika Utara` = as.list(setNames(
      amerika_utara$kode,
      amerika_utara$negara
    )),
    Asia = as.list(setNames(asia$kode, asia$negara)),
    Eropa = as.list(setNames(eropa$kode, eropa$negara)),
    Oseania = as.list(setNames(oseania$kode, oseania$negara))
  )
  ## Pilihan banding_negara ----
  updateSelectizeInput(getDefaultReactiveDomain(),
    "banding_negara",
    choices = pilihan_input_negara
  )
  updateSelectizeInput(getDefaultReactiveDomain(),
    "cemas_pikir_negara",
    choices = pilihan_input_negara
  )
  updateSelectizeInput(getDefaultReactiveDomain(),
    "banding_negara_pros_kon",
    choices = pilihan_input_negara
  )
  updateSelectizeInput(getDefaultReactiveDomain(),
    "negara_setara",
    choices = pilihan_input_negara
  )
  updateSelectizeInput(getDefaultReactiveDomain(),
    "banding_negara_tren",
    choices = pilihan_input_negara
  )

  output$values <- renderPrint({
    list(
      banding_negara = input$banding_negara,
      cemas_pikir_negara = input$cemas_pikir_negara,
      banding_negara_pros_kon = input$banding_negara_pros_kon,
      negara_setara = input$negara_setara,
      banding_negara_tren = input$banding_negara_tren
    )
  })
  ## teks_peringkat ----
  output$teks_peringkat <- renderText({
    # Mempersiapkan data
    data <- data_literasi %>%
      filter(negara != "OECD") %>%
      group_by(literasi) %>%
      mutate(
        peringkat = as.integer(
          rank(-rerata, ties.method = "average")
        )
      )
    # Menentukan peringkat
    mapel <- input$mapel
    teks_mapel <- ifelse(
      mapel == "mat", "Matematika",
      ifelse(
        mapel == "baca", "Membaca",
        "Sains"
      )
    )
    peringkat <- data %>%
      filter(negara == "Indonesia" & literasi == teks_mapel)
    peringkat$peringkat
  })

  ## teks_rerata ----
  output$teks_rerata <- renderText({
    # Menentukan rerata
    rerata_mat <- data_literasi %>%
      filter(negara == "Indonesia" & literasi == "Matematika")
    rerata_baca <- data_literasi %>%
      filter(negara == "Indonesia" & literasi == "Membaca")
    rerata_sains <- data_literasi %>%
      filter(negara == "Indonesia" & literasi == "Sains")

    if (input$mapel == "mat") {
      rerata_mat$rerata
    } else if (input$mapel == "baca") {
      rerata_baca$rerata
    } else if (input$mapel == "sains") {
      rerata_sains$rerata
    }
  })

  ## teks_sd ----
  output$teks_sd <- renderText({
    # Menentukan simpangan baku
    sd_mat <- data_literasi %>%
      filter(negara == "Indonesia" & literasi == "Matematika")
    sd_baca <- data_literasi %>%
      filter(negara == "Indonesia" & literasi == "Membaca")
    sd_sains <- data_literasi %>%
      filter(negara == "Indonesia" & literasi == "Sains")

    if (input$mapel == "mat") {
      sd_mat$sd
    } else if (input$mapel == "baca") {
      sd_baca$sd
    } else {
      sd_sains$sd
    }
  })
  ## Fungsi gabung ----
  gabung <- function(grup, individu) {
    lima_atas <- c("SGP", "JPN", "KOR", "EST", "CHE")
    asia_tenggara <- c("BRN", "MYS", "VNM", "SGP", "KHM", "THA", "PHL")
    pop_besar <- c("USA", "MEX", "PHL", "BRA")
    pilih_negara <- individu
    hasil <- c("IDN")
    for (anggota in grup) {
      if (anggota == "lima_atas") {
        hasil <- c(hasil, lima_atas)
      } else if (anggota == "asia_tenggara") {
        hasil <- c(hasil, asia_tenggara)
      } else if (anggota == "pop_besar") {
        hasil <- c(hasil, pop_besar)
      } else if (anggota == "pilih_negara") {
        hasil <- c(hasil, individu)
      }
    }

    return(hasil)
  }
  ## plot_literasi ----
  output$plot_literasi <- renderPlot({
    oecd_literasi <- input$oecd_literasi
    banding <- input$banding
    banding_negara <- input$banding_negara
    # Teks mapel
    mapel <- input$mapel
    teks_mapel <- ifelse(mapel == "mat", "Matematika",
      ifelse(mapel == "baca", "Membaca",
        "Sains"
      )
    )
    # Memfilter data
    data_filter <- gabung(banding, banding_negara)
    data1 <- data_literasi %>%
      filter(kode_negara %in% data_filter)
    data_oecd <- data_literasi %>%
      filter(kode_negara == "OECD")
    rerata_oecd <- data_oecd %>%
      filter(literasi == teks_mapel)
    if (oecd_literasi == TRUE) {
      data <- rbind(data1, data_oecd)
      alfa <- 1
    } else {
      data <- data1
      alfa <- 0
    }
    # Plot
    data %>%
      filter(literasi == teks_mapel) %>%
      mutate(idn = ifelse(kode_negara == "IDN", TRUE, FALSE)) %>%
      pivot_longer(
        cols = starts_with("pstl_") & !ends_with("_se"),
        names_to = "pstl",
        values_to = "skor"
      ) %>%
      mutate(negara = fct_reorder(negara, -rerata)) %>%
      ggplot(aes(x = negara)) +
      geom_line(aes(y = skor, group = negara, color = idn),
        linewidth = 1.5
      ) +
      geom_hline(
        yintercept = rerata_oecd$rerata,
        alpha = alfa, linetype = "dashed"
      ) +
      geom_point(aes(y = skor), size = 2) +
      geom_point(aes(y = rerata), size = 3.5) +
      theme_bw(base_size = 14) +
      scale_color_brewer(palette = "Dark2") +
      scale_x_discrete(labels = scales::label_wrap(10)) +
      theme(
        axis.title.x = element_blank(),
        legend.position = "none",
        plot.caption = element_text(color = "gray50", size = 9)
      ) +
      labs(
        y = paste0("Skor Literasi ", teks_mapel),
        caption = "Catatan: Lima titik kecil merepresentasikan persentil 10, 25, 50 (median),\n75, dan 90. Titik yang lebih besar merupakan rerata."
      )
  })
  ## plot_variasi ----
  output$plot_variasi <- renderPlot({
    # Filter negara-negara terpilih
    banding <- input$banding
